private void mostrarFuncionario(ArrayList<Funcionario> listaFuncionario) {
        String col[] = {"Código", "Nome","CPF"};
        DefaultTableModel tableModel = new DefaultTableModel(col, 0);
        for (Funcionario c : listaFuncionario) {
            Object[] data = {
                c.getId(), 
                c.getNome(),
            c.getCpf()
            };
            tableModel.addRow(data);
        }        
        Tabela = new JTable(tableModel);
        Painel.setViewportView(Tabela);
    }
private void mostrarFuncionarioTudo(ArrayList<Funcionario> listaFuncionario) {
        String col[] = {"Código", "Nome","CPF","Senha"};
        DefaultTableModel tableModel = new DefaultTableModel(col, 0);
        for (Funcionario f : listaFuncionario) {
            Object[] data = {
                f.getId(), 
                f.getNome(),
            f.getCpf(),
            f.getSenha()
            };
            tableModel.addRow(data);
        }        
        Tabela = new JTable(tableModel);
        Painel.setViewportView(Tabela);
    }
private void mostrarErro(String x) {
        String col[] = {x};
        DefaultTableModel tableModel = new DefaultTableModel(col, 0); 
        Tabela = new JTable(tableModel);
        Painel.setViewportView(Tabela);
    }
