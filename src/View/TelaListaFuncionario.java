    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
         ArrayList listaF = null;
        if(campoID.getText().length()>0){
           listaF = controller.buscaTodosID(Integer.parseInt(campoID.getText()));
        mostrarFuncionario(listaF);
        }else if (campoNome.getText().length()>0){
            listaF = controller.buscarNome(campoNome.getText());
        mostrarFuncionario(listaF);  
        }else listaF = controller.buscarTodos();
        mostrarFuncionario(listaF);
        if(listaF.size()<=0){
            mostrarErro("Dado não encotrado");
        }
    }//GEN-LAST:event_btBuscarActionPerformed

