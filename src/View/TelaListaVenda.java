private void mostrarVenda(ArrayList<Venda> listaVenda) {
        String col[] = {"Código Venda", "Código Produto","Código Funcionario","Preço","Quantidade","Data"};
        DefaultTableModel tableModel = new DefaultTableModel(col, 0);
        for (Venda v : listaVenda) {
            Object[] data = {
                v.getId(), 
                v.getIdProduto(),
            v.getIdFuncionario(),
            v.getPreco(),
            v.getQuantidade(),
            v.getData()
            };
            tableModel.addRow(data);
        }        
        Tabela = new JTable(tableModel);
        Painel.setViewportView(Tabela);
    }
