    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        if(campoIDFun.getText().length()>0){
            mostrarVenda(controller.buscarTodosIDF(Integer.parseInt(campoIDFun.getText())));
        }else if(campoIDProd.getText().length()>0){
            mostrarVenda(controller.buscarTodosIDP(Integer.parseInt(campoIDProd.getText())));
        }else if(campoData.getText().length()>0){
            mostrarVenda(controller.buscarTodosDT(campoData.getText()));
        }else mostrarVenda(controller.buscarTodos());
        
    }//GEN-LAST:event_btBuscarActionPerformed

