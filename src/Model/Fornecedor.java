package Model;

/**
 *
 * @author Dcxz
 */
public class Fornecedor {
 private int id;
 private String nome;
 private String cep;
 private String cnpj;

    public Fornecedor(int id, String nome, String cep, String cnpj) {
        this.id = id;
        this.nome = nome;
        this.cep = cep;
        this.cnpj = cnpj;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }
 
 
}
